function findTitle(id) {
  return library.find((item) => item.id === id) || library[0];
}

function queryMatches(item) {
  if (!state.query) {
    return true;
  }

  const haystack = [
    item.title,
    item.synopsis,
    item.logline,
    item.type,
    item.badge,
    ...item.genres,
    ...item.tags,
    ...item.cast
  ]
    .join(" ")
    .toLowerCase();

  return haystack.includes(state.query.toLowerCase());
}

function filterMatches(item) {
  if (state.filter === "All") {
    return true;
  }

  if (state.filter === "Series") {
    return item.type === "Series";
  }

  if (state.filter === "Films") {
    return item.type === "Film";
  }

  return item.genres.includes(state.filter);
}

function getVisibleTitles() {
  return library.filter((item) => queryMatches(item) && filterMatches(item));
}

function syncSelectedTitle(visibleTitles) {
  if (!visibleTitles.length) {
    return findTitle(state.selectedId);
  }

  const stillVisible = visibleTitles.some((item) => item.id === state.selectedId);
  if (stillVisible) {
    return findTitle(state.selectedId);
  }

  state.selectedId = visibleTitles[0].id;
  state.note = `${visibleTitles[0].title} is now featured because it matches your current browse filters.`;
  return visibleTitles[0];
}

function setTheme(item) {
  document.documentElement.style.setProperty("--hero-accent", item.accent);
  document.documentElement.style.setProperty("--hero-glow", item.glow);
  document.documentElement.style.setProperty("--hero-gradient", item.heroArt);
  heroBackdrop.style.background = item.heroArt;
}

function renderHero(item) {
  setTheme(item);
  heroBadge.textContent = item.badge;
  heroTitle.textContent = item.title;
  heroMatch.textContent = `${item.match}% Match`;
  heroYear.textContent = String(item.year);
  heroMaturity.textContent = item.maturity;
  heroDuration.textContent = item.duration;
  heroDescription.textContent = item.synopsis;
  heroNote.textContent = state.note;
  listButton.textContent = state.myList.has(item.id) ? "In My List" : "+ My List";

  heroTags.innerHTML = item.genres
    .concat(item.tags)
    .slice(0, 5)
    .map((tag) => `<span>${tag}</span>`)
    .join("");

  spotlightTitle.textContent = item.title;
  spotlightBlurb.textContent = `${item.logline} Cast: ${item.cast.join(", ")}.`;
}

function renderSidebar(visibleTitles) {
  const activeContinue = visibleTitles
    .filter((item) => item.progress > 0)
    .sort((a, b) => b.progress - a.progress)
    .slice(0, 3);

  continueCount.textContent = `${activeContinue.length} active`;
  continueList.innerHTML = activeContinue.length
    ? activeContinue
        .map(
          (item) => `
            <article class="continue-item">
              <div class="continue-item-head">
                <span class="continue-title">${item.title}</span>
                <span class="continue-small">${item.progress}% watched</span>
              </div>
              <div class="progress-bar"><div class="progress-fill" style="width:${item.progress}%"></div></div>
            </article>
          `
        )
        .join("")
    : `<p class="sidebar-text">No in-progress titles match the current search.</p>`;

  const queuedTitles = library
    .filter((item) => state.myList.has(item.id))
    .sort((a, b) => b.match - a.match)
    .slice(0, 4);

  queueList.innerHTML = queuedTitles.length
    ? queuedTitles
        .map(
          (item) => `
            <article class="queue-item">
              <div>
                <div class="queue-title">${item.title}</div>
                <div class="queue-meta">${item.type} | ${item.genres.slice(0, 2).join(" / ")}</div>
              </div>
              <span class="card-score">${item.match}%</span>
            </article>
          `
        )
        .join("")
    : `<p class="sidebar-text">Your list is empty. Add a title from the hero or a card below.</p>`;
}

function renderFilters() {
  filterChips.innerHTML = filterOptions
    .map(
      (filter) => `
        <button class="filter-chip ${filter === state.filter ? "active" : ""}" type="button" data-filter="${filter}">
          ${filter}
        </button>
      `
    )
    .join("");
}

function buildCard(item) {
  const selected = item.id === state.selectedId ? "selected" : "";
  const progressMarkup = item.progress
    ? `
      <div class="card-progress">
        <span>${item.progress}% completed</span>
        <div class="progress-bar"><div class="progress-fill" style="width:${item.progress}%"></div></div>
      </div>
    `
    : `<span class="queue-meta">${item.tags[0]}</span>`;

  return `
    <button class="title-card ${selected}" type="button" data-title-id="${item.id}" style="--card-art:${item.cardArt};">
      <div class="poster-art"></div>
      <div class="card-copy">
        <div class="card-header">
          <h3 class="card-title">${item.title}</h3>
          <span class="card-badge">${item.type}</span>
        </div>
        <div class="card-meta">
          <span class="card-score">${item.match}% Match</span>
          <span>${item.year}</span>
          <span>${item.maturity}</span>
          <span>${item.duration}</span>
        </div>
        <p class="card-description">${item.logline}</p>
        <div class="card-footer">
          <span class="queue-meta">${item.genres.join(" / ")}</span>
          ${progressMarkup}
        </div>
      </div>
    </button>
  `;
}

function renderRows(visibleTitles) {
  let rowCount = 0;

  const markup = rowConfigs
    .map((row) => {
      const items = visibleTitles.filter((item) => row.match(item));
      if (!items.length) {
        return "";
      }

      rowCount += 1;
      return `
        <section class="content-row">
          <div class="row-head">
            <div>
              <h3 class="row-title">${row.name}</h3>
              <p class="row-helper">${row.description}</p>
            </div>
            <span class="sidebar-small">${items.length} title${items.length === 1 ? "" : "s"}</span>
          </div>
          <div class="row-track">
            ${items.map((item) => buildCard(item)).join("")}
          </div>
        </section>
      `;
    })
    .join("");

  rowsShell.innerHTML = markup || `
    <section class="empty-state">
      <h3>No titles found</h3>
      <p>Try a shorter search term or switch to another filter. The clone is wired locally, so every result is generated from the in-page library.</p>
    </section>
  `;

  return rowCount;
}

function renderResults(visibleTitles, rowCount) {
  if (!visibleTitles.length) {
    resultsCopy.textContent = state.query
      ? `No titles match \"${state.query}\" yet. Clear the search or switch filters to widen the catalog.`
      : "No titles are available for this filter right now.";
    return;
  }

  resultsCopy.textContent = `${visibleTitles.length} titles across ${rowCount} collections. Search and filters update the hero and every row instantly.`;
}

function render() {
  const visibleTitles = getVisibleTitles();
  const featuredTitle = syncSelectedTitle(visibleTitles);

  renderFilters();
  renderHero(featuredTitle);
  renderSidebar(visibleTitles);
  const rowCount = renderRows(visibleTitles);
  renderResults(visibleTitles, rowCount);
}

searchInput.addEventListener("input", (event) => {
  state.query = event.target.value.trim();
  state.note = state.query
    ? `Showing local results for \"${state.query}\".`
    : "Select a title to update the feature banner.";
  render();
});

